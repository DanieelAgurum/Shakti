    <p>Kaizen se compromete a proteger la privacidad y confidencialidad de la información personal de sus clientes y usuarios de la plataforma Shakti, así como de la información sensible relacionada con la prevención de la violencia hacia la mujer. Para ello, se establecen las siguientes políticas de privacidad y confidencialidad de la información:</p>

    <section class="policy-section">
      <h2>1. Información personal recopilada</h2>
      <p>Se recopila la siguiente información personal: nombre, correo electrónico, número de teléfono, ubicación general (sin datos específicos), y respuestas a tests de detección de violencia (emocional, física, económica, etc.), así como datos de interacción en foros y chats (anónimos o privados).</p>
    </section>

    <section class="policy-section">
      <h2>2. Uso de la información personal</h2>
      <p>La información personal se utilizará para: brindar apoyo emocional y orientación personalizada, facilitar la conexión con especialistas y otras usuarias, generar planes de acción personalizados, enviar recursos informativos (artículos, videos, guías prácticas), y mejorar la experiencia de la aplicación. No se usará para fines comerciales ni publicitarios.</p>
    </section>

    <section class="policy-section">
      <h2>3. Compartir información personal</h2>
      <p>La información personal no se compartirá con terceros, excepto en los siguientes casos: (a) con especialistas o autoridades gubernamentales cuando sea necesario para garantizar la seguridad de la usuaria, (b) con proveedores de servicios que apoyen el funcionamiento de Shakti bajo estrictos acuerdos de confidencialidad, y (c) cuando la usuaria lo autorice expresamente para recibir apoyo adicional.</p>
    </section>

    <section class="policy-section">
      <h2>4. Seguridad de la información personal</h2>
      <p>Se implementarán medidas de seguridad como: encriptación de datos, acceso restringido a la información solo para personal autorizado, capacitación regular del equipo Kaizen sobre manejo de datos sensibles, y auditorías periódicas para garantizar la protección de la información, especialmente en situaciones de riesgo.</p>
    </section>

    <section class="policy-section">
      <h2>5. Derechos de los titulares de la información personal</h2>
      <p>Los titulares de la información personal tienen los siguientes derechos (derechos ARCO): acceso a sus datos personales, rectificación de información incorrecta, cancelación de datos cuando no sean necesarios, y oposición al uso de sus datos para fines específicos. Para ejercer estos derechos, las usuarias pueden contactar al responsable de privacidad.</p>
    </section>

    <section class="policy-section">
      <h2>6. Cambios en el aviso de privacidad</h2>
      <p>Kaizen se reserva el derecho de modificar este aviso de privacidad en cualquier momento. Cualquier cambio será notificado a los titulares de la información personal a través de la plataforma Shakti o por correo electrónico, con al menos 15 días de antelación.</p>
    </section>

    <section class="policy-section">
      <h2>7. Almacenamiento y retención de datos</h2>
      <p>La información se almacenará de manera segura en servidores protegidos y se retendrá solo durante el tiempo necesario para cumplir con los propósitos establecidos, o según lo requiera la ley. Las usuarias pueden solicitar la eliminación de sus datos en cualquier momento.</p>
    </section>

    <section class="policy-section">
